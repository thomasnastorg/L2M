                                    </br><h3> IMPLANTATION DES BAIES DE BRASSAGE </h3></br>
                                    <p>
                                        Ce dispositif répond à la volonté des pouvoirs publics de développer, en faveur des associations, une gamme de simplifications et de services en matière d'aide à l'emploi, comme la gestion des paies et l'assistance comptable. Ce service est offert à toutes les associations de moins de 10 ETP : clubs sportifs, associations culturelles, etc.

                                        Impact Emploi est un logiciel développé par l' URSSAF qui permet d'établir le bulletin de salaire et de fournir l'ensemble des déclarations sociales et fiscales, qu'elles soient mensuelles, trimestrielles ou annuelles. Tout cela, simplement à partir de quelques données : le nombre d'heures travaillées, leur nature et les éléments de rémunération bruts ou nets (salaire, prime, avantages en nature, indemnités de frais, etc.). Ce logiciel est confié gratuitement à des tiers de confiance qui exécutent les opérations pour le compte de l'association. Le tiers de confiance est souvent une structure qui fédère le milieu dans lequel évolue l'association. Ainsi, le CROSL a reçu le label « CRIB » (Centre de Ressources et d’Information des Bénévoles) pour devenir tiers de confiance pour l’URSSAF qui met à notre disposition ce fameux logiciel. Nous demandons 60 € de cotisation annuelle par association et nous facturons 5 € par fiche de paie. Nous produisons bien évidemment toute la panoplie légale de documents : récapitulatifs périodiques, déclarations URSSAF et ASSEDIC, organismes de retraite. Nous pouvons gérer des virements automatiques : prélèvement sur le compte des associations et versement sur le compte des salariés ou encore prélèvement direct sur les comptes des associations au crédit des organismes sociaux. En 2009, nous avions 72 associations abonnées pour un total de 332 salariés, ce qui représentait 2,8 millions d’euros de masse salariale, soit 15 % de la masse salariale totale du sport lorrain.

                                        En tant que centre labellisé « CRIB » nous apportons également des conseils aux associations qui emploient du personnel. Ces conseils portent sur le respect de la législation, sur le remboursement des déplacements, sur les litiges éventuels. Nous disposons de toute l’information utile pour mener à bien cette mission de conseil.

                                        En ce moment, sous l’impulsion du ministère, nous œuvrons au développement de l’usage du chèque emploi associatif.
